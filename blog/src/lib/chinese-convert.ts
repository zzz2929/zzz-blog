// Build reverse mapping
export const t2sMap: Record<string, string> = {};
for (const [s, t] of Object.entries(s2tMap)) {
  if (s !== t) t2sMap[t] = s;
}

/**
 * Convert text between simplified and traditional Chinese.
 * @param text - The text to convert
 * @param direction - 's2t' for simplified→traditional, 't2s' for traditional→simplified
 */
export function convertChinese(text: string, direction: 's2t' | 't2s'): string {
  const map = direction === 's2t' ? s2tMap : t2sMap;
  let result = '';
  for (const char of text) {
    result += map[char] || char;
  }
  return result;
}
